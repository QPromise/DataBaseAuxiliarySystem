package cn.edu.ouc.pojo.dto;

/**
 * 一级指标元信息
 * 
 * @author 高杨
 * @date 2018年1月21日 上午9:45:52
 */
public class FirstIndexMetadata {

	// 1.id
	private Integer id;
	// 2.指标名称
	private String designation;
	// 3.行政区
	private String district;

	public FirstIndexMetadata() {
	}

	public FirstIndexMetadata(String designation, String district) {
		this.designation = designation;
		this.district = district;
	}
	
	public FirstIndexMetadata(String designation) {
		this.designation = designation;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getDesignation() {
		return designation;
	}

	public void setDesignation(String designation) {
		this.designation = designation;
	}

	public String getDistrict() {
		return district;
	}

	public void setDistrict(String district) {
		this.district = district;
	}
}
